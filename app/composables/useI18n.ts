const locale = ref<Locale>('en')
let initialized = false

export function useI18n() {
  if (!initialized && typeof localStorage !== 'undefined') {
    const saved = localStorage.getItem('locale')
    if (saved === 'uk' || saved === 'en') locale.value = saved
    initialized = true
  }

  function t(key: string, params?: Record<string, string | number>): string {
    let s = messages[locale.value][key] ?? messages.en[key] ?? key
    if (params) for (const k in params) s = s.replace(`{${k}}`, String(params[k]))
    return s
  }

  function setLocale(l: Locale) {
    locale.value = l
    if (typeof localStorage !== 'undefined') localStorage.setItem('locale', l)
  }

  return { locale, t, setLocale }
}
